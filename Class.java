public class Person {
    // Fields
    String name;
    int age;

    // Constructor
    public Person(String n, int a) {
        name = n;
        age = a;
    }

    // Method
    void greet() {
        System.out.println("Hello, my name is " + name + " and I'm " + age + " years old.");
    }
}
